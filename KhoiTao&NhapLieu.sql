----------------------------------------------TAO BANG & CAI DAT KHOA CHINH, NGOAI------------------------------------------------
--TẠO BẢNG GIAOVIEN
CREATE TABLE GIAOVIEN
( 
	 MAGV CHAR(3) NOT NULL, -- SET MAGV LÀ NOT NULL
	 HOTEN NVARCHAR(40), 
	 LUONG FLOAT, 
	 PHAI NCHAR(3), 
	 NGSINH DATE, 
	 DIACHI NVARCHAR(200),
	 GVQLCM CHAR(3), 
	 MABM NCHAR(4),
	 PRIMARY KEY (MAGV) -- KHÓA CHÍNH MAGV
)


--TẠO BẢNG GV_DT 
CREATE TABLE GV_DT(
	MAGV CHAR(3) NOT NULL,
	DIENTHOAI CHAR(10) NOT NULL,
	PRIMARY KEY(MAGV, DIENTHOAI) -- KHÓA CHINH MAGV,DIENTHOAI
)


--TẠO BẢNG NGUOITHAN
CREATE TABLE NGUOITHAN
(
	MAGV CHAR(3) NOT NULL,
	TEN NVARCHAR(10) NOT NULL,
	NGSINH DATE,
	PHAI NCHAR(3),
	PRIMARY KEY (MAGV, TEN) -- KHÓA CHÍNH MAGV,TEN
	--, FOREIGN KEY (MAGV) REFERENCES GIAOVIEN(MAGV)
)


--TẠO BẢNG KHOA
CREATE TABLE KHOA
(
	MAKHOA CHAR(4) NOT NULL,
	TENKHOA NVARCHAR(20),
	NAMTL INT,
	PHONG CHAR(3),
	DIENTHOAI CHAR(10),
	TRUONGKHOA CHAR(3),
	NGAYNHANCHUC DATE,
	PRIMARY KEY (MAKHOA) -- KHÓA CHÍNH MAKHOA
	--, FOREIGN KEY (TRUONGKHOA) REFERENCES GIAOVIEN(MAGV) 
)


--TẠO BẢNG BOMON
CREATE TABLE BOMON
(
	MABM NCHAR(4) NOT NULL,
	TENBM NVARCHAR(20),
	PHONG CHAR(3),
	DIENTHOAI CHAR(10),
	TRUONGBM CHAR(3),
	MAKHOA CHAR(4),
	NGAYNHANCHUC DATE,
	PRIMARY KEY (MABM),
	--FOREIGN KEY (TRUONGBM) REFERENCES GIAOVIEN(MAGV),
	--FOREIGN KEY (MAKHOA) REFERENCES KHOA(MAKHOA) 
)


-- TẠO VẢNG CHUDE
CREATE TABLE CHUDE(
	MACD NCHAR(4) NOT NULL,
	TENCHD NCHAR (40),
	PRIMARY KEY(MACD)
)


-- TẠO BẢNG DETAI
CREATE TABLE DETAI(
	MADT CHAR (3) NOT NULL,
	TENDT NCHAR (40),
	CAPQL NCHAR (10),
	KINHPHI FLOAT,
	NGAYBD DATE,
	NGAYKT DATE,
	MACD NCHAR (4),
	GVCNDT CHAR (3),
	PRIMARY KEY (MADT) -- KHÓA CHÍNH MADT
)


-- TẠO VẢNG CONGVIEC
CREATE TABLE CONGVIEC(
	MADT CHAR(3) NOT NULL,
	SOTT INT NOT NULL,
	TENCV NCHAR (40),
	NGAYBD DATE,
	NGAYKT DATE, 
	PRIMARY KEY(MADT, SOTT) -- KHÓA CHÍNH MADT,SOTT
)


-- TẠO BẢNG THAMGIADT
CREATE TABLE THAMGIADT(
	MAGV CHAR(3) NOT NULL,
	MADT CHAR (3) NOT NULL,
	STT INT NOT NULL,
	PHUCAP FLOAT,
	KETQUA NCHAR(3),
	PRIMARY KEY(MAGV, MADT, STT) -- KHÓA CHÍNH MAGV,MADT,STT
)



